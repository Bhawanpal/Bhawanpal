package com.InfyTraining.DTO;

public class TraineeDTO {
	

	private Integer traineeId;
	private String traineeName;
	private String traineeTech;
	private String traineeEmail;
	
	public Integer getTraineeId() {
		return traineeId;
	}
	public void setTraineeId(Integer traineeId) {
		this.traineeId = traineeId;
	}
	public String getTraineeName() {
		return traineeName;
	}
	public void setTraineeName(String traineeName) {
		this.traineeName = traineeName;
	}
	public String getTraineeTech() {
		return traineeTech;
	}
	public void setTraineeTech(String traineeTech) {
		this.traineeTech = traineeTech;
	}
	public String getTraineeEmail() {
		return traineeEmail;
	}
	public void setTraineeEmail(String traieeEmail) {
		this.traineeEmail = traieeEmail;
	}
	
}
